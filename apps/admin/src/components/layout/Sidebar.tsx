"use client";

import Link from "next/link";
import { usePathname } from "next/navigation";
import { cn } from "@noopdaa/ui";

const navigation = [
  { name: "대시보드", href: "/dashboard", icon: DashboardIcon },
  { name: "포스트", href: "/posts", icon: PostIcon },
  { name: "카테고리", href: "/categories", icon: CategoryIcon },
  { name: "태그", href: "/tags", icon: TagIcon },
  { name: "미디어", href: "/media", icon: MediaIcon },
  { name: "댓글", href: "/comments", icon: CommentIcon },
];

export function Sidebar() {
  const pathname = usePathname();

  return (
    <aside className="flex w-64 flex-col border-r border-gray-200 bg-white dark:border-gray-700 dark:bg-gray-800">
      <div className="flex h-16 items-center justify-center border-b border-gray-200 dark:border-gray-700">
        <Link href="/dashboard" className="text-xl font-bold text-primary-600">
          Noopdaa Blog
        </Link>
      </div>

      <nav className="flex-1 space-y-1 p-4">
        {navigation.map((item) => {
          const isActive = pathname === item.href || pathname.startsWith(`${item.href}/`);
          return (
            <Link
              key={item.name}
              href={item.href}
              className={cn(
                "flex items-center gap-3 rounded-lg px-3 py-2 text-sm font-medium transition-colors",
                isActive
                  ? "bg-primary-50 text-primary-600 dark:bg-primary-900/20 dark:text-primary-400"
                  : "text-gray-700 hover:bg-gray-100 dark:text-gray-300 dark:hover:bg-gray-700"
              )}
            >
              <item.icon className="h-5 w-5" />
              {item.name}
            </Link>
          );
        })}
      </nav>
    </aside>
  );
}

